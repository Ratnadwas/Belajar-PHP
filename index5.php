<?php

// perulangan
// for
// while
// do while 

// 1. for 
for ($i = 1; $i <= 10; $i++){
    echo "angka ke $i <br>";
}
for ($b = 10; $b >= 0; $b--){
    echo "angka $b <br>";
}


// 2. while

$k = 0;
while ($k <= 10) {
    echo "Bilang ke $k <br>";
    $k++;
}

// 3. do while 

$x = 0;
do{
    echo "Bilangan ke $x <br>";
    $x++;
} while ($x <= 10);

?>